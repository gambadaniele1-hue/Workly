// ===== SEZIONE 1: LOGICA DI PROCESSO =====
declare(strict_types=1);

session_start();

$_SESSION = [];

if (ini_get('session.use_cookies')) {
    $params = session_get_cookie_params();
    setcookie(
        session_name(),
        '',
        time() - 42000,
        $params['path'],
        $params['domain'],
        (bool)$params['secure'],
        (bool)$params['httponly']
    );
}


// ===== SEZIONE 2: LOGICA DI PROCESSO =====
session_destroy();
http_response_code(204);